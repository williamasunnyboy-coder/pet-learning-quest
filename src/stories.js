export function getDailyStory(petType) {
  const stories = PET_STORIES[petType] || PET_STORIES['west-highland']
  const d = new Date()
  const dayOfYear = Math.floor((d - new Date(d.getFullYear(), 0, 0)) / 86400000)
  return stories[dayOfYear % stories.length]
}

export function getOverflowChapter(petType, taskName, petName, index) {
  const templates = OVERFLOW_TEMPLATES[petType] || OVERFLOW_TEMPLATES['west-highland']
  const pick = templates[(taskName.length + index) % templates.length]
  return pick.replace(/{taskName}/g, taskName).replace(/{petName}/g, petName)
}

export function fillStory(text, petName) {
  return text.replace(/{petName}/g, petName)
}

export function fillChapter(text, petName, taskName) {
  return text
    .replace(/{petName}/g, petName)
    .replace(/{taskName}/g, taskName)
}
